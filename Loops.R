################################Repeat Loop###########################################

#Syntax:
# repeat { 
#   commands 
#   if(condition) {
#     break
#   }
# }

v <- c("Hello","loop")
cnt <- 2

repeat {
  print(v)
  cnt <- cnt+1
  
  if(cnt > 5) {
    break
  }
}


######################################While Loop#############################################

#Syntax: 
#while (test_expression) {
#  statement
#}

v <- c("Hello","while loop")
cnt <- 2

while (cnt < 7) {
  print(v)
  cnt = cnt + 1
}


####################################for loop##################################################

#Syntax:
#  for (value in vector) {
#  statements
#}

v <- LETTERS[1:4]
for ( i in v) {
  print(i)
}

